char	*ft_strlowcase(char *str)
{
	int i;

	i = 0;
	while (*str != '\0')
	{
		if (65 <= *str && *str <= 90)
			*str = *str + 32;
		str++;
		i++;
	}
	while (i > 0)
	{
		str--;
		i--;
	}
	return (str);
}
