int		ft_str_is_numeric(char *str)
{
	int i;

	i = 0;
	if (*str == '\0')
		return (1);
	while (*str != '\0')
	{
		if (*str <= 47 || *str >= 58)
			return (0);
		str++;
	}
	return (1);
}
