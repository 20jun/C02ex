char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (*str != '\0')
	{
		if (97 <= *str && *str <= 122)
			*str = *str - 32;
		str++;
		i++;
	}
	while (i > 0)
	{
		str--;
		i--;
	}
	return (str);
}
